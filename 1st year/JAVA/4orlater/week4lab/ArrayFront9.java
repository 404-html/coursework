public class ArrayFront9 {
    public static boolean arrayFront9(int[] nums) {
	boolean a = false;
	int l;
	if (nums.length < 4){
	 l = nums.length;}else{l = 4;}
        for (int i = 0 ; i < l ; i++) {
         a = a || (nums[i] == 9);}
	return a;	 
    }

    public static void main(String[] args) {
        int N = args.length;
        int[] nums = new int[N];
        for (int i = 0; i < N; i++) {
            nums[i] = Integer.parseInt(args[i]);
        }
        System.out.println(arrayFront9(nums));
    }
}
